        <h1 class="h1_article_section">Welkom bij Markant</h1>
        <p class="p_article_section">Markant ondersteunt mantelzorgers bij hun intensieve werk. Mantelzorgers kunnen bij
          ons onder meer terecht voor informatie, hulp, advies, cursussen en trainingen. Ook kan Markant een
          vrijwilliger inzetten, zodat mantelzorgers even de handen vrij hebben om op te laden. Je bent mantelzorger als
          je langdurig en onbetaald zorgt voor een naaste met een lichamelijke of verstandelijke beperking, dementie,
          een chronische ziekte of die terminaal ziek is. Hieronder een overzicht van de ondersteuning die Markant kan
          bieden.</p>
