        <h1>Ons verhaal</h1>
        <div class="text_text-foto">
          <p>
            In Amsterdam zijn veel mensen die dag en nacht klaar staan voor hun zieke naaste. Mantelzorger ben je
            meestal niet voor even en de zorg is vaak intensief. Je leven verandert en je besteedt veel tijd met en aan
            je zieke naaste. Dat doe je graag en met je hart, maar het kan ook zwaar zijn.
          </p>
          <p>
            Markant helpt om de zorg vol te houden. Dat doen we samen met onze vrijwilligers. Die kunnen je tijdelijk
            bijstaan en je net even die ruimte geven om zelf te ontspannen en weer op te laden. We maken contact, gaan
            in gesprek en zoeken naar nieuwe oplossingen. Zo helpen we de mantelzorger om in balans te komen en te
            blijven.
          </p>
          <p class="txt_weg">
            Onze kennis en ervaring delen we met beroepskrachten in de zorg en andere organisaties. Zodat ook zij
            mantelzorgers kunnen ondersteunen.
          </p>
          <p class="txt_weg">
            Elke dag helpen we Amsterdammers om de zorg voor een partner, een kind, een familielid of vriend vol te
            houden. Omdat we weten dat iedereen soms een steuntje in de rug nodig heeft.
          </p>
